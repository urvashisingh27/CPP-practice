#include <iostream>
using namespace std;

int main()
{
    int a;
    char c;
    float f;
    double d;
    cout << "size of int: " << sizeof(a)<< endl;
    cout << "size of char: " << sizeof(c)<< endl;
    cout << "size of double: " << sizeof(d)<< endl;
    cout << "size of float: " << sizeof(f)<< endl;

    return 0;
}